let tabButtons = document.querySelectorAll(".tab-container .button-container button");// this will hold all buttons
        let tabPanel = document.querySelectorAll(".tab-container .tab-panel");//this will hold all panels
        function showpanel(panelindex,color)
        {
          tabButtons.forEach(function(element)
          {
              element.style.backgroundColor="";
          });
          tabButtons[panelindex].style.backgroundColor = color;
          tabPanel.forEach(function(element)
          {
              element.style.display="none";
          });
          tabPanel[panelindex].style.display="block";
  
        }
        showpanel(0,'rgb(235, 244, 250)')
        let stck = document.querySelectorAll(".stocks .stock");
        function highlight(index,color)
        {
          stck.forEach(function(element){
            element.style.backgroundColor="";
          });
          stck[index].style.backgroundColor=color;
        }
        highlight(0,'rgb(235, 244, 250)');
